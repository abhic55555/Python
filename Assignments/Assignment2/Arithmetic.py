def add(no1,no2):
    a= no1+no2
    print("Addition of {} and {} is {}".format(no1,no2,a))
    
def sub(no1,no2):
    s= no1-no2
    print("Subtraction of {} and {} is {}".format(no1,no2,s))
    
def mult(no1,no2):
    m= no1*no2
    print("Multiplication of {} and {} is {}".format(no1,no2,m))
    
def div(no1,no2):
    d= no1/no2
    print("Division of {} and {} is {}".format(no1,no2,d))
	
